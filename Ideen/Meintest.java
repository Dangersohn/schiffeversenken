public class Meintest {

  public static void main(String[] args) {

   System.out.println("\n  0123456789"); // Leerzeile + Beschriftung
   for (int i=0;i<10;i++){
     System.out.print(i+" ");
     for (int j=0;j<10;j++){
        System.out.print("-");
       }
     System.out.println(); // Zeilenwechsel
   }
}

} // Ende 